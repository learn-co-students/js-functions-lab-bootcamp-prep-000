function happyHolidays() {
  return "Happy holidays!"
}

function happyHolidaysTo(firstName) {
  return `Happy holidays, ${firstName}!`
}

function happyHolidayTo(holiday, firstName) {
  return `Happy ${holiday}, ${firstName}!`
}

function holidayCountdown(holiday, quantity) {
  return `It\'s ${quantity} days until ${holiday}!`
}






