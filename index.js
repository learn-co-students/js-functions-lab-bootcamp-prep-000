function happyHolidays() {return `Happy holidays!`}
function happyHolidaysTo(individual) {return `Happy holidays, ${individual}!`}
function happyHolidayTo(holiday, individual) {return `Happy ${holiday}, ${individual}!`}
function holidayCountdown(holiday, daysUntil) {return `It's ${daysUntil} days until ${holiday}!`}
