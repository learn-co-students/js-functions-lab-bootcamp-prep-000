function happyHolidays() {
  return ("Happy holidays!")
}
function happyHolidaysTo(Ronald) {
  return (`Happy holidays, ${Ronald}!`)
}
function happyHolidayTo(Christmas, Ronald) {
  return (`Happy ${Christmas}, ${Ronald}!`)
}
function holidayCountdown(Christmas, five) {
  return (`It's ${five} days until ${Christmas}!`)
}
