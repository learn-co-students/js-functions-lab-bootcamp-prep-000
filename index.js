// returns "Happy Holidays!"
function happyHolidays() {
  return "Happy holidays!";
}

// wish someone a happy holiday (unspecified)
function happyHolidaysTo(name) {
  return "Happy holidays, " + name + "!";
}

// wish someone a happy holiday (specified)
function happyHolidayTo(h, p) {
  return "Happy " + h + ", " + p + "!";
}

// return a string with number of days left to holiday
function holidayCountdown(h, n) {
  return "It's " + n + " days until " + h + "!";
}
