// Returns the string "Happy Holidays"
function happyHolidays() {
  return "Happy holidays!"
}

// Accepts a parameter "name" and returns the string Happy Holidays "name"
function happyHolidaysTo(name) {
  return `Happy holidays, ${name}!`
}

// Accepts parameters for "holiday" and "name" then returns the String Happy
// "holiday", "name"
function happyHolidayTo(holiday, name) {
  return `Happy ${holiday}, ${name}!`
}

// Accepts parameters for "holiday" and "days" till the holiday then returns
// the String It's "days" days until "holiday"!
function holidayCountdown(holiday, days) {
  return `It's ${days} days until ${holiday}!`
}
