function happyHolidays() {
  return "Happy holidays!"; // general holidays greeting
}

function happyHolidaysTo(name) {
  return `Happy holidays, ${name}!`; // targeted holiday greeting
}

function happyHolidayTo(holiday, name) {
  return `Happy ${holiday}, ${name}!`; // targeted greeting for a specific holiday
}

function holidayCountdown(holiday, days) {
  return `It'\s ${days} days until ${holiday}!`; // countdown until a specific holiday
}
