function happyHolidaysTo(name) {
  greeting = `Happy holidays, ${name}!`
  return greeting
}

function happyHolidayTo(holiday, name) {
  greeting = `Happy ${holiday}, ${name}!`
  return greeting
}

function holidayCountdown(holiday, days) {
  countdown = `It's ${days} days until ${holiday}!`
  return countdown
}